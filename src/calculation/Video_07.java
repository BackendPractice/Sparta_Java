package calculation;

public class Video_07 {
    public static void main(String[] args) {
        // if ~ else
        boolean isFlag = false;

        if (isFlag) {
            System.out.println("if문 실행됨");
        } else {
            System.out.println("else문 실행됨");
        }

        // if ~ else if ~ else
        int num = 2;

        if (num == 1) {
            System.out.println("if문 실행됨");
        } else if (num == 2) {
            System.out.println("else if문 실행됨");
        } else {
            System.out.println("else문 실행됨");
        }
    }
}
