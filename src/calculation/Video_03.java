package calculation;

public class Video_03 {
    public static void main(String[] args) {
        // 대입 연산
        int num = 10;

        num = num + 2;
        System.out.println(num);

        num = num - 5;
        System.out.println(num);

        System.out.println("------------------");

        // 복합 대입 연산
        num = 10;

        num += 2;
        System.out.println(num);

        num -= 5;
        System.out.println(num);

        System.out.println("------------------");
        num = 10;

        num ++;
        System.out.println(num);

        num --;
        System.out.println(num);
    }
}
